using System.Collections.Generic;

namespace ConDep.Dsl.Core
{
    public class ConDepWebSiteSettings
    {
        private readonly string _webSiteName;
        private readonly List<ConDepWebSiteBinding> _bindings = new List<ConDepWebSiteBinding>();

        public ConDepWebSiteSettings(string webSiteName)
        {
            _webSiteName = webSiteName;
        }

        public string WebSiteName
        {
            get { return _webSiteName; }
        }

        public List<ConDepWebSiteBinding> Bindings
        {
            get {
                return _bindings;
            }
        }
    }
}